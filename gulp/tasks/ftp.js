//=======================================================================================================================================================================================================================================================
import log from "fancy-log";
import vinylFTP from "vinyl-ftp";
import config from "../config/ftp.js";
import gulp from "gulp";
import path from "../config/path.js";
import {notifyError} from "../exports/notifyError.js";
//=======================================================================================================================================================================================================================================================
/** Загрузка папки с проектом на сервер */
const uploadToServer = () => {
   config.log = log;
   const ftpConnect = vinylFTP.create(config);
   return gulp.src([`${path.distF}/**/*.*`, `${path.distF}/**/.*`, `${path.distF}/**/*.`, `${path.distF}/**/*`])
      .pipe(notifyError("FTP"))
      .pipe(ftpConnect.dest(path.ftp));
};
//=======================================================================================================================================================================================================================================================
export default uploadToServer;
