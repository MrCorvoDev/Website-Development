/**
 * * Автоматически изменяет размер textarea
 * Использует autosize.js
 * HTML Структура: .js_e-autosize
 */
//=======================================================================================================================================================================================================================================================
import _dom from "../../../exports/dom.js";
//=======================================================================================================================================================================================================================================================
const textareas = [..._dom.get.all("autosize")];
(async () => {
   const {default: autosize} = await import(/* webpackPreload: true */ "autosize");

   textareas.forEach(textarea => autosize(textarea));
})();
